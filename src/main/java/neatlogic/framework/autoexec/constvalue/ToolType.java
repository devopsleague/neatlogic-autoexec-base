package neatlogic.framework.autoexec.constvalue;

public enum ToolType {
    TOOL("tool", "工具"),
    SCRIPT("script", "脚本");
    private String value;
    private String text;

    private ToolType(String value, String text) {
        this.value = value;
        this.text = text;
    }

    public String getValue() {
        return value;
    }

    public String getText() {
        return text;
    }
}
